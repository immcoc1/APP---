public class ItemLit {

    String untranslated;
    String translated;

    public ItemLit(String untranslated, String translated) {
        this.untranslated = untranslated;
        this.translated = translated;
    }

    public ItemLit(){

    }

    public String getUntranslated() {
        return untranslated;
    }

    public String getTranslated() {
        return translated;
    }


}